from selenium import webdriver
from selenium.webdriver.common.keys import Keys
import time
from getpass import getpass 
from selenium.webdriver.firefox.firefox_binary import FirefoxBinary


class TwitterBot:
    def __init__(self,username,password):

        self.username = username
        self.password = password 
        binary = FirefoxBinary('C:\\Users\\*****\\AppData\\Local\\Mozilla Firefox\\firefox.exe')
        self.bot = webdriver.Firefox(firefox_binary=binary)

    def login(self):
        bot = self.bot
        bot.get('https://www.instagram.com/accounts/login/?hl=en&source=auth_switcher')
        time.sleep(3)
        email = bot.find_element_by_class_name('pexuQ')
        password = bot.find_element_by_name('password')
        email.clear()
        password.clear()
        email.send_keys(self.username)
        password.send_keys(self.password)
        password.send_keys(Keys.RETURN)
        time.sleep(5)
        
        
